public class OBAccountQueryWhere {
	private long CurrencyID = 0;			// 币种
	private long OfficeID = 0;			// 办事处
	private long AccountTypeID = 0;		// 账户类型ID
	private long ChildClientID = 0;		// 下属单位ID
	private String ChildAccountNO = "";	// 下属单位账号
	private Timestamp DateTo = null;		// 截至日期

	/**
	 * Returns the accountTypeID.
	 * @return long
	 */
	public long getAccountTypeID() {
		return AccountTypeID;
	}

	/**
	 * Returns the childAccountNO.
	 * @return String
	 */
	public String getChildAccountNO() {
		return ChildAccountNO;
	}

	/**
	 * Returns the childClientID.
	 * @return long
	 */
	public long getChildClientID() {
		return ChildClientID;
	}

	/**
	 * Returns the currencyID.
	 * @return long
	 */
	public long getCurrencyID() {
		return CurrencyID;
	}

	/**
	 * Returns the dateTo.
	 * @return Timestamp
	 */
	public Timestamp getDateTo() {
		return DateTo;
	}

	/**
	 * Returns the officeID.
	 * @return long
	 */
	public long getOfficeID() {
		return OfficeID;
	}

	/**
	 * Sets the accountTypeID.
	 * @param accountTypeID The accountTypeID to set
	 */
	public void setAccountTypeID(long accountTypeID) {
		AccountTypeID = accountTypeID;
	}

	/**
	 * Sets the childAccountNO.
	 * @param childAccountNO The childAccountNO to set
	 */
	public void setChildAccountNO(String childAccountNO) {
		ChildAccountNO = childAccountNO;
	}

	/**
	 * Sets the childClientID.
	 * @param childClientID The childClientID to set
	 */
	public void setChildClientID(long childClientID) {
		ChildClientID = childClientID;
	}

	/**
	 * Sets the currencyID.
	 * @param currencyID The currencyID to set
	 */
	public void setCurrencyID(long currencyID) {
		CurrencyID = currencyID;
	}

	/**
	 * Sets the dateTo.
	 * @param dateTo The dateTo to set
	 */
	public void setDateTo(Timestamp dateTo) {
		DateTo = dateTo;
	}

	/**
	 * Sets the officeID.
	 * @param officeID The officeID to set
	 */
	public void setOfficeID(long officeID) {
		OfficeID = officeID;
	}

}