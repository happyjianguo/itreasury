	// 查询结果
	/* 放款通知单号 */
	private String payCode = "";

	/* 还款通知单号 */
	private String repayCode = "";

	/* 还款明细备注 */
	private String sdetailremarks = "";
	/* 合同编号 */
	private String scontractcodeID = "";
