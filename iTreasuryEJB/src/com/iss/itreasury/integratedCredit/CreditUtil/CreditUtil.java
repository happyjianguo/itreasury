package com.iss.itreasury.integratedCredit.CreditUtil;

public class CreditUtil 
{
   private CreditUtil(){}
   public static final String traDouble(String strTemp)
   {
	   String str[] = new String[strTemp.length()];
	   String dstring ="";
	   for(int i=0;i<strTemp.length();i++)

		{

			str=strTemp.split(",");			
		}
	   for(int l=0;l<str.length;l++)
		{
		   dstring = dstring+str[l];
		}
	   return dstring;
   }
  public static void main(String [] a)
  {
	  System.out.println(CreditUtil.traDouble("3,333,333,33.00!34,333,333.00"));
  }
}
