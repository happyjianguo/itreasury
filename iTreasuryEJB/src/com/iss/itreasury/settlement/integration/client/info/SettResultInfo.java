package com.iss.itreasury.settlement.integration.client.info;

import java.io.Serializable;

public class SettResultInfo implements Serializable {
	private String transNo= null;	//结算交易号
	private long   status = -1;		//结算交易状态标记 1：有效 0：无效
	private String message = null;	//结算交易状态描述 
	private String moduleType = null;  //模块类型
	
	public String getTransNo() {
		return transNo;
	}
	public void setTransNo(String transNo) {
		this.transNo = transNo;
	}
	public long getStatus() {
		return status;
	}
	public void setStatus(long status) {
		this.status = status;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getModuleType() {
		return moduleType;
	}
	public void setModuleType(String moduleType) {
		this.moduleType = moduleType;
	}

}
